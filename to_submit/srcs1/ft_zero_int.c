#include "ft_printf.h"

void	ft_zero_int(char *str, t_list *list)
{
	int	i;

	i = 0;
	if (list->space)
		i++;
	while (str[i])
	{
		if (str[i] == ' ')
			str[i] = '0';
		else if (str[i] == '-' || str[i] == '+')
		{
			str[0] = str[i];
			if (i != 0)
				str[i] = '0';
		}
		i++;
	}
}
