#include "ft_printf.h"

void	ft_zero_sharp(char *str, int low_high)
{
	int	i;

	str[0] = '0';
	if (low_high == 1)
		str[1] = 'X';
	else
		str[1] = 'x';
	i = 2;
	while (str[i] == '0' || str[i] == 'x' || str[i] == 'X' || str[i] == ' ')
	{
		str[i] = '0';
		i++;
	}
}
